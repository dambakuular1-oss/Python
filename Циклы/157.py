# Задание №1
print("Задание №1 — чётные числа от 1 до 100:")
for num in range(2, 101, 2):  # range(2, 101, 2) — начинаем с 2, до 100, шаг 2
    print(num)

# Задание №2
print("\nЗадание №2 — числа от -10 до 10:")
for num in range(-10, 11):  # range(-10, 11) — от -10 (включительно) до 11 (не включая), т.е. до 10
    print(num)

# Задание №3
print("\nЗадание №3 — последовательность с шагом -3:")
for num in range(20, 1, -3):  # range(20, 1, -3) — начинаем с 20, до 2 (1 не включаем), шаг -3
    print(num)
