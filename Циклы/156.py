# Задание №1
print("Задание №1 — числа от 1 до 10:")
for num in range(1, 11):  # range(1, 11) генерирует числа от 1 до 10 включительно
    print(num)

# Задание №2
print("\nЗадание №2 — числа от 20 до 10:")
for num in range(20, 9, -1):  # range(20, 9, -1) — начинаем с 20, до 10, шаг -1
    print(num)

# Задание №3
print("\nЗадание №3 — список чисел от 1 до 5:")
numb = list(range(1, 6))  # range(1, 6) даёт числа 1, 2, 3, 4, 5; list() преобразует в список
print(numb)

