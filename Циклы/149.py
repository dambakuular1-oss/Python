# Задание №1: вывести элементы множества до первого отрицательного числа
tst1 = {1, 3, 6, 7, -9, 12}
for num in tst1:
    if num < 0:
        break  # прерываем цикл при встрече отрицательного числа
    print(num)

# Задание №2: сумма элементов списка до первого нуля
tst2 = [7, 1, 2, 5, 0, 3, 9]
sum = 0
for num in tst2:
    if num == 0:
        break  # останавливаемся, когда встретили ноль
    sum+= num
print(sum)

# Задание №3: сформировать список цифр числа до цифры 6
tst3 = 897654
re = []
for di in str(tst3):
    digit = int(di)
    if digit == 6:
        break  # прекращаем сбор цифр, когда встретили 6
    re.append(digit)
print(re)
